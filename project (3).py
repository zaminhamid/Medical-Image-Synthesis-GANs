    def __init__(self, root_dir, transform=None, class_map=None):
        self.root_dir = root_dir
        self.transform = transform
        self.samples = []

        # Default label mapping if not provided
        if class_map is None:
            class_map = {"health": 0, "tb": 1, "sick": 2}

