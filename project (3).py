# ---------------------------------------------------------------
#                 1. Self-Attention Block
# ---------------------------------------------------------------
class SelfAttention(nn.Module):
    def __init__(self, in_dim):
        super().__init__()
        self.query = nn.Conv2d(in_dim, in_dim // 8, 1)
        self.key   = nn.Conv2d(in_dim, in_dim // 8, 1)
        self.value = nn.Conv2d(in_dim, in_dim, 1)

        self.gamma = nn.Parameter(torch.zeros(1))

    def forward(self, x):
        B, C, H, W = x.size()

        query = self.query(x).view(B, -1, H*W)
        key   = self.key(x).view(B, -1, H*W)
        attn  = torch.bmm(query.permute(0, 2, 1), key)
        attn  = torch.softmax(attn, dim=-1)

        value = self.value(x).view(B, C, H*W)
        out = torch.bmm(value, attn.permute(0, 2, 1))
        out = out.view(B, C, H, W)

        return self.gamma * out + x


# ---------------------------------------------------------------
#              2. Conv Blocks (Upsample / Downsample)
# ---------------------------------------------------------------
class GenBlock(nn.Module):
    def __init__(self, in_c, out_c):
        super().__init__()
        self.main = nn.Sequential(
            nn.Upsample(scale_factor=2),
            nn.Conv2d(in_c, out_c, 3, 1, 1),
            nn.LeakyReLU(0.2),
            nn.Conv2d(out_c, out_c, 3, 1, 1),
            nn.LeakyReLU(0.2)
        )
    def forward(self, x):
        return self.main(x)


class DiscBlock(nn.Module):
    def __init__(self, in_c, out_c):
        super().__init__()
        self.main = nn.Sequential(
            nn.Conv2d(in_c, out_c, 3, 1, 1),
            nn.LeakyReLU(0.2),
            nn.Conv2d(out_c, out_c, 3, 1, 1),
            nn.LeakyReLU(0.2),
            nn.AvgPool2d(2)
        )
    def forward(self, x):
        return self.main(x)


# ---------------------------------------------------------------
#              3. Progressive Generator
# ---------------------------------------------------------------
class ProgressiveGenerator(nn.Module):
    def __init__(self, nz, num_classes):
        super().__init__()
        self.nz = nz
        self.embed = nn.Embedding(num_classes, nz)

        self.init = nn.ConvTranspose2d(nz, 512, 4, 1, 0)

        # channels for each stage
        ch = [512, 256, 128, 64, 32]

        self.blocks = nn.ModuleList([
            GenBlock(ch[0], ch[1]),  # 4→8   (output ch[1]=256)
            GenBlock(ch[1], ch[2]),  # 8→16  (output ch[2]=128)
            GenBlock(ch[2], ch[3]),  # 16→32 (output ch[3]=64)
            GenBlock(ch[3], ch[4]),  # 32→64 (output ch[4]=32)
            GenBlock(ch[4], ch[4]),  # 64→128 (output ch[4]=32)
        ])

        # add Self-Attention at 64×64 and 128×128
        self.att_64 = SelfAttention(ch[4])
        #self.att_128 = SelfAttention(ch[4])

        # final output layers per resolution (to RGB)
        # These take the OUTPUT of the corresponding GenBlock
        self.toRGB = nn.ModuleList([
            nn.Conv2d(ch[1], 1, 1),   # 8x8 output from blocks[0] (ch[1] channels)
            nn.Conv2d(ch[2], 1, 1),   # 16x16 output from blocks[1] (ch[2] channels)
            nn.Conv2d(ch[3], 1, 1),   # 32x32 output from blocks[2] (ch[3] channels)
            nn.Conv2d(ch[4], 1, 1),   # 64x64 output from blocks[3] (ch[4] channels)
            nn.Conv2d(ch[4], 1, 1),   # 128x128 output from blocks[4] (ch[4] channels)
        ])

    def forward(self, z, labels, stage, alpha):
        emb = self.embed(labels)
        z = torch.add(z, emb) # Fix: Use torch.add to ensure an out-of-place operation
        x = self.init(z.unsqueeze(2).unsqueeze(3)) # (B, 512, 4, 4)

        # Store the output of the *previous* block, for blending purposes
        # This will hold the feature map BEFORE current stage's block is applied
        x_for_prev_rgb = None

        for i in range(stage + 1):
            if i == stage and stage > 0: # If it's not the first stage, and this is the current stage's block
                x_for_prev_rgb = x # x is currently the output of blocks[stage-1]

            x = self.blocks[i](x) # x is now output of blocks[i] (and potentially attention)
            if i == 3: x = self.att_64(x)
            #if i == 4: x = self.att_128(x)

        # After the loop, `x` is the output of `self.blocks[stage]` (and potentially attention)
        current_res_output_features = x

        out = self.toRGB[stage](current_res_output_features) # Current stage output

        if stage > 0 and alpha < 1.0:
            # `x_for_prev_rgb` holds the output of `self.blocks[stage-1]`
            # Apply `toRGB[stage-1]` to `x_for_prev_rgb`
            prev = self.toRGB[stage-1](x_for_prev_rgb)
            prev_upsampled = nn.functional.interpolate(prev, scale_factor=2, mode='nearest')
            out = alpha*out + (1-alpha)*prev_upsampled

        return torch.tanh(out)


# ---------------------------------------------------------------
#           4. Progressive Discriminator (WGAN-GP)
# ---------------------------------------------------------------
class ProgressiveDiscriminator(nn.Module):
    def __init__(self, num_classes):
        super().__init__()

        ch = [32, 64, 128, 256, 512]

        self.fromRGB = nn.ModuleList([
            nn.Conv2d(1, ch[0], 1),   # 8x8 input to ch[0] (32 channels)
            nn.Conv2d(1, ch[1], 1),   # 16x16 input to ch[1] (64 channels)
            nn.Conv2d(1, ch[2], 1),   # 32x32 input to ch[2] (128 channels)
            nn.Conv2d(1, ch[3], 1),   # 64x64 input to ch[3] (256 channels)
            nn.Conv2d(1, ch[4], 1),   # 128x128 input to ch[4] (512 channels)
        ])

        self.blocks = nn.ModuleList([
            DiscBlock(ch[4], ch[3]), # 128 -> 64 (512 -> 256 channels)
            DiscBlock(ch[3], ch[2]), # 64 -> 32 (256 -> 128 channels)
            DiscBlock(ch[2], ch[1]), # 32 -> 16 (128 -> 64 channels)
            DiscBlock(ch[1], ch[0]), # 16 -> 8 (64 -> 32 channels)
        ])

        # ModuleList for channel adaptation convolutions for blending
        self.channel_adapt_convs = nn.ModuleList()
        for s in range(1, len(ch)):
            # Map from ch[s-1] to ch[s] channels
            self.channel_adapt_convs.append(nn.Conv2d(ch[s-1], ch[s], 1))

        # The final convolutional layer should output features matching the embedding dimension
        # After all DiscBlocks, x is (B, ch[0], 8, 8) i.e. (B, 32, 8, 8)
        # To get (B, 128, 1, 1) to flatten to (B, 128), use Conv2d(ch[0], 128, 8, 1, 0)
        self.final = nn.Sequential(
            nn.Conv2d(ch[0], 128, 8, 1, 0),
        )

        self.embed = nn.Embedding(num_classes, 128)

    def forward(self, img, labels, stage, alpha):

        # Current stage's feature map (e.g., stage 1, ch[1]=64 channels)
        x_curr_res_features = self.fromRGB[stage](img)

        if stage > 0 and alpha < 1.0:
            # Downsample the input image to the previous resolution
            downsampled_img = nn.functional.avg_pool2d(img, 2)
            # Apply the previous stage's fromRGB to the downsampled image
            x_prev_raw_features = self.fromRGB[stage-1](downsampled_img) # Has ch[stage-1] channels

            # Upsample spatially to match current resolution's spatial dimensions
            x_prev_upsampled_spatially = nn.functional.interpolate(x_prev_raw_features, size=x_curr_res_features.shape[2:], mode='nearest')

            # Adapt channels of x_prev_upsampled_spatially to match x_curr_res_features
            x_prev_upsampled_and_adapted_channels = self.channel_adapt_convs[stage-1](x_prev_upsampled_spatially)

            # Blend the current stage's features with the channel-adapted previous stage's features
            x = alpha * x_curr_res_features + (1-alpha) * x_prev_upsampled_and_adapted_channels
        else:
            # If no blending or stage 0, just use the current stage's features
            x = x_curr_res_features

        # Process through the appropriate blocks
        # Loop from the block corresponding to the current resolution, down to the 8x8 block.
        # Corrected loop: iterate from 'stage' down to 1.
        # For stage=0, this loop (range(0,0,-1)) will not execute, which is correct.
        for i in range(stage, 0, -1):
            # The blocks are indexed such that blocks[3] handles 16->8, blocks[2] handles 32->16, etc.
            # So, for a given stage `i`, the block to apply is `self.blocks[len(self.blocks) - i]` (or 4-i as in original)
            x = self.blocks[4 - i](x)

        # Apply the final convolution and flatten
        x = self.final(x)
        x = x.view(x.size(0), -1)

        # conditional projection
        emb = self.embed(labels)
        out = (x * emb).sum(dim=1)

